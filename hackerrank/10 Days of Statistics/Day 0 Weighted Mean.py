def weightedMean(X, W):
    # Write your code here
    n=len(X)
    sum=0
    sum1=0
    for x in range(0,n,1):
        sum=sum+(X[x]*W[x])
        sum1=sum1+W[x]

    print(round(sum/sum1,1))

